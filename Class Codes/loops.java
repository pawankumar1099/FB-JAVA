public class loops {
    public static void main(String[] args) {
        //for loop

        for(int i=1;i<=5;i++){
            System.out.println(i);
        }

        //while loop

        int j = 0;
        while (j<5) {
            System.out.println(j);
        }

        //do-while loop

        int k=0;
        do{
            System.out.println(k);
            k++;
        }while(k<5);
    }
}


